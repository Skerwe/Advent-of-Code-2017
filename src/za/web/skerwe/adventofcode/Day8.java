    private int part1(String input) {
        HashMap<String, Integer> registers = new HashMap<>();
        String[] instructions = input.split("\n");

        for (String instruction : instructions) {
            process(instruction, registers);
        }

        int largest = 0;
        for (Integer value : registers.values()) {
            if (value > largest) {
                largest = value;
            }
        }

        return largest;
    }

    private void process(String instruction, HashMap<String, Integer> registers) {
        String[] commands = instruction.split(" ");

        Integer registerValue = 0;
        if (registers.get(commands[0]) != null) {
            registerValue = registers.get(commands[0]);
        }

        Integer registerValueLocal;
        switch (commands[5]) {
            case ">" :
                registerValueLocal = getCompareValue(registers.get(commands[4]));
                if (registerValueLocal > Integer.parseInt(commands[6])) {
                    registerValue = incOrDec(commands, registerValue);
                }
                break;
            case "<" :
                registerValueLocal = getCompareValue(registers.get(commands[4]));
                if (registerValueLocal < Integer.parseInt(commands[6])) {
                    registerValue = incOrDec(commands, registerValue);
                }
                break;
            case ">=" :
                registerValueLocal = getCompareValue(registers.get(commands[4]));
                if (registerValueLocal >= Integer.parseInt(commands[6])) {
                    registerValue = incOrDec(commands, registerValue);
                }
                break;
            case "<=" :
                registerValueLocal = getCompareValue(registers.get(commands[4]));
                if (registerValueLocal <= Integer.parseInt(commands[6])) {
                    registerValue = incOrDec(commands, registerValue);
                }
                break;
            case "!=" :
                registerValueLocal = getCompareValue(registers.get(commands[4]));
                if (registerValueLocal != Integer.parseInt(commands[6])) {
                    registerValue = incOrDec(commands, registerValue);
                }
                break;
            case "==" :
                registerValueLocal = getCompareValue(registers.get(commands[4]));
                if (registerValueLocal == Integer.parseInt(commands[6])) {
                    registerValue = incOrDec(commands, registerValue);
                }
                break;
            default: System.err.println("Unknown command ["+commands[5]+"]");
        }

        if (registerValue > highestValue) {
            highestValue = registerValue;
        }

        registers.put(commands[0], registerValue);
    }

    private int getCompareValue(Integer value) {
        if (value == null) {
            value = 0;
        }
        return value;
    }

    private int incOrDec(String[] commands, Integer registerValue) {
        if (commands[1].equals("inc")) {
            registerValue += Integer.parseInt(commands[2]);
        } else {
            registerValue -= Integer.parseInt(commands[2]);
        }
        return registerValue;
    }
